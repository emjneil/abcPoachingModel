eleExt <- function ( initialYear, 
                     finalYear, 
                     growthRate, 
                     popInitial, 
                     carryingCapacity,
                     poachDeaths, 
                     spike_min=21,
                     spike_max=64,
                     spike_prob=0.15,
                     spike_duration_in_years=3) 
{
  #compute ahead of time which years will be spikes
  spike_years<-generate_spike_years(2000,2100,
                                    spike_prob = spike_prob,
                                    spike_duration_in_years = spike_duration_in_years)
  # this is where we will store the populations each year
  populations = rep(0, 1 + finalYear - initialYear)
  # the first element is the initial population
  populations[1] = popInitial
  # for all the other years....
  for (year in 2:length(populations)) 
  { 
    # x = rand number [0,1]
    x = runif(n=1,min=0,max=1)
    if (spike_years[year] == FALSE) {
      populations[year] = 
        populations[year-1] + growthRate*(1-(populations[year-1])/carryingCapacity)*(populations[year-1]) - runif(n=1,min=poachDeaths/2,
                                                                                                                  max = poachDeaths*2)
    }
    else {
      #fixed spike mortality
      populations[year] = 
        populations[year-1] + growthRate*(1-(populations[year-1])/carryingCapacity)*(populations[year-1]) - runif(n=1,min=spike_min,
                                                                                                                  max = spike_max)
    }
  }
  # return the vector
  return(populations)
}




