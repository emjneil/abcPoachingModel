# k figured out based on the 0.058 growth rate estimate from Demeke (2008)

eleExt <- function ( initialYear, finalYear, growthRate, popInitial, poachDeaths, carryingCapacity ) 
{
  # this is where we will store the populations each year
  populations = rep(0, 1 + finalYear - initialYear)
  # the first element is the initial population
  populations[1] = popInitial
  # for all the other years.... logistic growth
  for (year in 2:length(populations)) 
  { 
    populations[year] = 
      populations[year-1] + growthRate*(1-(populations[year-1])/carryingCapacity)*(populations[year-1]) - 
      runif(n=1,min=poachDeaths/2,
            max = poachDeaths*2)
  }
  # return the vector
  return(populations)
}

